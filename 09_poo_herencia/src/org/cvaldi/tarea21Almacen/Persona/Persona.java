package org.cvaldi.tarea21Almacen.Persona;

public class Persona {
    private String nombre;
    private String nie;

    public Persona(String nombre, String nie) {
        this.nombre = nombre;
        this.nie = nie;
    }

    public String getNombre() {
        return nombre;
    }

    public String getNie() {
        return nie;
    }
}
